package org.haikuos.haikudepotserver.job.model;

/**
 * <p>This enum describes the type of the job data.</p>
 */

public enum JobDataType {

    /**
     * <p>Supplied data is data that has been provided as an input for a job.</p>
     */

    SUPPLIED,

    /**
     * <p>Generated data is data that has been produced as output from a job.</p>
     */

    GENERATED

}
